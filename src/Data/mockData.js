// ==================== HELPER FUNCTIONS ====================
export const getArticleBySlug = (slug) => articles.find(a => a.slug === slug);
export const getArticlesByCategory = (categorySlug) => articles.filter(a => a.categorySlug === categorySlug);
export const getRelatedArticles = (article, limit = 3) => {
  if (!article?.relatedPosts?.length) {
    return articles.filter(a => a.category === article?.category && a.id !== article?.id).slice(0, limit);
  }
  return articles.filter(a => article.relatedPosts.includes(a.id));
};
export const getArticlesByTag = (tag) => articles.filter(a => a.tags.includes(tag));
export const getIssueBySlug = (slug) => issues.find(i => i.slug === slug);
export const getContributorById = (id) => contributors.find(c => c.id === id);